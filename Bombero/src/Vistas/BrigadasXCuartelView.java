    private void jcCuartelesItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcCuartelesItemStateChanged
        limpiarTabla();
        ArrayList<Cuartel> cuarteles = new ArrayList<>();
        BrigadaData bd = new BrigadaData();
        CuartelData cd = new CuartelData();
        cuarteles = cd.listarCuartel();

        if (jcCuarteles.getSelectedIndex() != 0) {
            int num = jcCuarteles.getSelectedIndex();
            Cuartel c = cuarteles.get(num - 1);
            ArrayList<Brigada> brigadas = bd.listarBrigadasPorCuartel(c.getCodigo());
            for (Brigada brigada : brigadas) {
                if (brigada.isLibre()) {
                    modelo.addRow(new Object[]{brigada.getNombre(), brigada.getEspecialidad(), "SI"});
                } else {
                    modelo.addRow(new Object[]{brigada.getNombre(), brigada.getEspecialidad(), "NO"});
                }

            }
        }
    }//GEN-LAST:event_jcCuartelesItemStateChanged


    private void armarCabecera() {
        modelo.addColumn("Nombre");
        modelo.addColumn("Especialidad");
        modelo.addColumn("Asignada");       
        jtBrigadasXCuartel.setModel(modelo);
    }
    public void llenarCombobox() {
        Cuartel c = new Cuartel(); 
        CuartelData cd = new CuartelData();        
        ArrayList<Cuartel> cuarteles = new ArrayList<>();        
        cuarteles = cd.listarCuartel();
        jcCuarteles.addItem("Seleccione Cuartel");
        for (Cuartel cu : cuarteles) {
            jcCuarteles.addItem(cu.toString());
        }
    }

    private void limpiarTabla() {
        for (int i = 0; i < jtBrigadasXCuartel.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
    }


    private void jbResolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbResolverActionPerformed
        SiniestroData sd = new SiniestroData();
        ArrayList<Siniestro> siniestros = sd.listarSiniestrosNoResueltosConBrigada();
        Siniestro s = new Siniestro();
        BrigadaData bd = new BrigadaData();
        try {
            s = siniestros.get(jcSiniestros.getSelectedIndex());
            s.setFechaResolucion(jdFechaResolucion.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate());
            Time t = new Time(Integer.parseInt(jcHora.getSelectedItem().toString()), Integer.parseInt(jcMinutos.getSelectedItem().toString()), 00);
            s.setHoraResolucion(t);
            s.setPuntuacion(Integer.parseInt(jcPuntuacion.getSelectedItem().toString()));
            s.setEstado(false);             
            sd.resolverSiniestro(s);           
            int c = s.getBrigada().getCodigo();
            Brigada b = bd.buscarBrigada(c);
            b.setLibre(true);
            bd.modificarBrigada(b);            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Complete todos los campos");
        }
        
        LimpiarCampos();
    }//GEN-LAST:event_jbResolverActionPerformed


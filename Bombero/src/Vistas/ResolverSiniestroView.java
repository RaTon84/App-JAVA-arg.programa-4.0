     private void llenarComboBox(){
        SiniestroData sd = new SiniestroData();
         ArrayList<Siniestro> siniestros= sd.listarSiniestrosNoResueltosConBrigada();
         for (Siniestro siniestro : siniestros) {    
            jcSiniestros.addItem(siniestro.toString());
        }
    }
     
    private void LimpiarCampos(){
        jcMinutos.setSelectedIndex(0);
        jcHora.setSelectedIndex(0);
        jcPuntuacion.setSelectedIndex(0);
        jdFechaResolucion.setDate(null);       
        jcSiniestros.removeAllItems();
        llenarComboBox();
    } 

